# 元组tuple
# 是Python中的一种内置数据类型，用于存储一组有序的元素。元组与列表类似，但它是不可变的，这意味着一旦创建，元组中的元素不能被修改、添加或删除。元组使用圆括号()来定义，元素之间用逗号分隔。
print("-----tuple-----")
t = (1, 2, 3, "hello", 3.14)
print(type(t))  # <class 'tuple'>
print(isinstance(t, tuple))  # True

# **核心特性：**
# - **有序**：元素有确定的顺序，可以通过索引访问。
# - **不可变**：一旦创建，不能修改、添加或删除其中的元素。
# - **可哈希**：因为不可变，所以元组本身可以作为字典的键或集合的元素（但要求其所有元素也必须是可哈希的）。
# - **异构**：可以包含不同类型的数据。
# - **可嵌套**：元组中可以包含列表、字典、甚至其他元组。

# **创建元组：**
print("-----创建元组-----")

# 使用圆括号创建元组
t1 = (1, 2, "hello", [3, 4, 5])  # 包含整数、字符串和列表的元组
t2 = 1, 2, 3  # 逗号才是元组的关键，圆括号只是为了明确表达
t3 = 5  # 整数5
t4 = (5,)  # 单元数元组

# 使用tuple()函数创建元组
# 通过可迭代对象创建元组
t5 = tuple([1, 2, 3, 4, 5])  # 从列表创建元组
t6 = tuple("hello")  # 从字符串创建元组

# 通过迭代器创建元组
t7 = tuple(range(5))  # 从迭代器创建元组

# 通过生成器表达式创建元组
t8 = tuple(x**2 for x in range(5))  # 从生成器表达式创建元组

print(t1)  # (1, 2, 'hello', [3, 4, 5])
print(t2)  # (1, 2, 3)
print(t3)  # 5
print(t4)  # (5,)
print(t5)  # (1, 2, 3, 4, 5)
print(t6)  # ('h', 'e', 'l', 'l', 'o')
print(t7)  # (0, 1, 2, 3, 4)
print(t8)  # (0, 1, 4, 9, 16)

# **索引与切片**
print("-----索引与切片-----")
# t = (1, 2, 3, "hello", 3.14)

# 通过索引访问
print(t[0])  # 1
print(t[-1])  # 3.14

# 通过索引进行切片，返回一个新的元组
# `tuple[start:stop:step]`，其中
# `start` 是切片的起始索引（默认为0），
# `stop` 是切片的结束索引（不包含，默认为元组长度），
# `step` 是切片的步长（默认为1）。
print(t[0:1])  # (1,)
print(t[1:4])  # (2, 3, 'hello')
print(t[-2:-1])  # ('hello',)
print(t[::])  # (1, 2, 3, 'hello', 3.14) # 从头到尾取元素
print(t[::1])  # (1, 2, 3, 'hello', 3.14) # 头到尾取元素
print(t[::2])  # (1, 3, 3.14) # 头到尾取元素,步长为2
print(t[::-1])  # (3.14, 'hello', 3, 2, 1) # 从尾到头取元素
print(t[::-2])  # (3.14, 3, 1) # 从尾到头取元素,步长为2

# **运算**
print("-----运算-----")
# t = (1, 2, 3, "hello", 3.14)

# 元组支持以下运算：

# - **连接**：使用 `+` 运算符连接两个元组，返回一个新的元组。
t9 = t + (6, 7)
print(t9)  # (1, 2, 3, 'hello', 3.14, 6, 7)
# print(t9-(6,7))  # TypeError: unsupported operand type(s) for -: 'tuple' and 'tuple'

# - **重复**：使用 `*` 运算符重复一个元组，返回一个新的元组。
t10 = t * 2
print(t10)  # (1, 2, 3, 'hello', 3.14, 1, 2, 3, 'hello', 3.14)

# - **比较**：元组可以进行比较，比较规则是逐元素比较，直到找到不同的元素为止。
print((1, 2) < (1, 3))  # True
print((1, 2) == (1, 2))  # True
print((1, 2) > (1, 1))  # True
print((1, 2) < (1, 2, 0))  # True
print((1, 2) > (1, 2, 0))  # False

# - **成员资格**：使用 `in` 运算符检查一个元素是否在元组中。
print(2 in t)  # True
print("world" in t)  # False
print("world" not in t)  # True

# **遍历**
print("-----遍历-----")
# t = (1, 2, 3, "hello", 3.14)
# 可以使用 `for in` 循环遍历元组中的元素。
for item in t:
    print(item)

# 带索引的遍历
# 使用 `enumerate()` 函数可以在遍历时获取元素的索引和值。
# `enumerate(t)` 返回一个迭代器，生成包含索引和值的元组。
# 例如，`for index, item in enumerate(t):` 将在每次迭代中将当前元素的索引赋值给 `index`，将当前元素的值赋值给 `item`。
for index, item in enumerate(t):
    print(f"Index: {index}, Item: {item}")
